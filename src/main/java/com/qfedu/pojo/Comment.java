package com.qfedu.pojo;


public class Comment {

    private Integer id;
    private String commenttop;
    private String commentmiddle;
    private String commentdwon;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCommenttop() {
        return commenttop;
    }

    public void setCommenttop(String commenttop) {
        this.commenttop = commenttop;
    }

    public String getCommentmiddle() {
        return commentmiddle;
    }

    public void setCommentmiddle(String commentmiddle) {
        this.commentmiddle = commentmiddle;
    }

    public String getCommentdwon() {
        return commentdwon;
    }

    public void setCommentdwon(String commentdwon) {
        this.commentdwon = commentdwon;
    }
}